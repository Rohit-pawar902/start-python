#implementing stack(Last In first OUT) using pop() and append() method
list=[]
#insertion at the last 
list.append(10)
print("inserted-:",list)
list.append(10)
print("inserted-:",list)
list.append(20)
print("inserted-:",list)
#POP from last
print("poped-:",list.pop()) 
print("poped-:",list.pop()) 
print("poped-:",list.pop()) 

